// --- slider ---
function setupSliderElement(container) {
  const images = container.getAttribute('data-images').split(',');
  let currentIndex = 0;

  const imgElement = container.querySelector('.slider-image');
  const prevBtn = container.querySelector('.prev');
  const nextBtn = container.querySelector('.next');

  let intervalId = null;

  function showImage(index) {
    if (index < 0) index = images.length - 1;
    if (index >= images.length) index = 0;
    currentIndex = index;
    imgElement.src = images[currentIndex];
  }

  prevBtn.addEventListener('click', () => {
    showImage(currentIndex - 1);
  });

  nextBtn.addEventListener('click', () => {
    showImage(currentIndex + 1);
  });

  imgElement.addEventListener('mouseenter', () => {
    if (intervalId) return; // evita più intervalli simultanei
    intervalId = setInterval(() => {
      showImage(currentIndex + 1);
    }, 1000);
  });

  imgElement.addEventListener('mouseleave', () => {
    clearInterval(intervalId);
    intervalId = null;
  });

  showImage(currentIndex);
}

function initAllSliders() {
  document.querySelectorAll('.slider').forEach(slider => {
    setupSliderElement(slider);
  });
}

// bouncing
function initBouncers() {
  const bouncers = document.querySelectorAll(".bouncer");

  bouncers.forEach(img => {
    let x = Math.random() * 400;
    let y = Math.random() * 300;
    let dx = (Math.random() * 1 + 0.5) * (Math.random() < 0.5 ? -1 : 1);
    let dy = (Math.random() * 1 + 0.5) * (Math.random() < 0.5 ? -1 : 1);

    const area = img.parentElement;

    function animate() {
      const areaRect = area.getBoundingClientRect();
      const imgRect = img.getBoundingClientRect();

      x += dx;
      y += dy;

      if (x <= 0 || x + imgRect.width >= areaRect.width) dx *= -1;
      if (y <= 0 || y + imgRect.height >= areaRect.height) dy *= -1;

      img.style.transform = `translate(${x}px, ${y}px)`;

      requestAnimationFrame(animate);
    }

    animate();
  });
}

// Esegui al caricamento della pagina
document.addEventListener("DOMContentLoaded", () => {
  initBouncers();
});


